PRICE_PREDICTION_TERMS = """### Price Forecast Guidance (ಬೆಲೆ ಮುನ್ಸೂಚನೆ ಮಾರ್ಗದರ್ಶನ)
Use these rules when talking about future prices:

- Separate current price from expected future price so farmers do not confuse them.
- Use words like rising, falling, stable, forecast, and confidence in simple Kannada.
- If the forecast is uncertain, say it clearly and avoid sounding like a guarantee.
- For hold/sell guidance, explain the reason in one short sentence after the forecast.

**Forecast Terms (ಮುನ್ಸೂಚನೆ ಪದಗಳು):**
- Price Forecast: ಬೆಲೆ ಮುನ್ಸೂಚನೆ (Bele Munsuchane)
- Predicted Price: ಅಂದಾಜು ಬೆಲೆ (Andaaju Bele)
- Current Price: ಪ್ರಸ್ತುತ ಬೆಲೆ (Prastuta Bele)
- Rising Trend: ಏರಿಕೆ ಪ್ರವೃತ್ತಿ (Erike Pravritti)
- Falling Trend: ಕುಸಿತ ಪ್ರವೃತ್ತಿ (Kusita Pravritti)
- Stable Trend: ಸ್ಥಿರ ಪ್ರವೃತ್ತಿ (Sthira Pravritti)
- Confidence: ವಿಶ್ವಾಸ ಮಟ್ಟ (Vishvaasa Matta)
- Sell Now: ಈಗಲೇ ಮಾರಾಟ ಮಾಡಿ (Eegale Maarata Maadi)
- Wait and Watch: ಕಾಯಿರಿ ಮತ್ತು ನೋಡಿ (Kaayiri Mattu Nodi)
- Risk of Drop: ಬೆಲೆ ಇಳಿಯುವ ಅಪಾಯ (Bele Iliyuva Apaaya)
"""
