"""
Report Generator — turns EvalResults into a markdown report + JSON metadata.

Responsibility: presentation layer only; no metric computation here.

Usage:
    generator = ReportGenerator()
    generator.generate(results, output_path=Path("reports/eval_2025.md"))
"""

from __future__ import annotations

from datetime import datetime, timezone
from pathlib import Path

from loguru import logger

from src.evaluation.models import EvalReportMeta, EvalResults

# Consistent emoji indicators used throughout the report
_OK = "✅"
_FAIL = "❌"


class ReportGenerator:
    """
    Generate a markdown evaluation report and a companion JSON metadata file.

    The markdown is human-readable; the JSON is machine-readable and can be
    imported into dashboards or CI gating logic.
    """

    def generate(
        self,
        results: EvalResults,
        output_path: Path | str = Path("eval_report.md"),
    ) -> Path:
        """
        Write the markdown report to *output_path* and a sibling .json file.

        Returns the path of the written markdown file.
        """
        out = Path(output_path)
        out.parent.mkdir(parents=True, exist_ok=True)

        md_content = self._build_markdown(results)
        out.write_text(md_content, encoding="utf-8")

        # Also persist metadata as JSON for programmatic consumption
        meta = self._build_meta(results)
        json_out = out.with_suffix(".json")
        json_out.write_text(meta.model_dump_json(indent=2), encoding="utf-8")

        logger.info(f"Evaluation report written to {out}")
        return out

    # ------------------------------------------------------------------
    # Private — markdown construction
    # ------------------------------------------------------------------

    def _build_markdown(self, results: EvalResults) -> str:
        targets = results.meets_targets()
        worst = results.worst_questions(5)
        timestamp = datetime.now(timezone.utc).isoformat()

        lines: list[str] = [
            "# CropFresh RAG Evaluation Report",
            "",
            f"**Generated:** {timestamp}  ",
            f"**Dataset:** `{results.dataset_path}`  ",
            f"**Questions evaluated:** {len(results.per_question)}  ",
            f"**Overall score:** `{results.overall_score:.3f}`",
            "",
            "---",
            "",
            "## Overall Metrics",
            "",
            "| Metric | Score | Target | Status |",
            "|--------|-------|--------|--------|",
            f"| Faithfulness | `{results.faithfulness:.3f}` | > 0.80 | {_OK if targets['faithfulness'] else _FAIL} |",
            f"| Answer Relevancy | `{results.answer_relevancy:.3f}` | > 0.75 | {_OK if targets['answer_relevancy'] else _FAIL} |",
            f"| Context Precision | `{results.context_precision:.3f}` | > 0.70 | {_OK if targets['context_precision'] else _FAIL} |",
            f"| Context Recall | `{results.context_recall:.3f}` | > 0.70 | {_OK if targets['context_recall'] else _FAIL} |",
            "",
            "---",
            "",
            "## Worst Performing Questions",
            "",
            "| # | Question | Avg Score | Faithfulness | Relevancy | Precision | Recall |",
            "|---|----------|-----------|--------------|-----------|-----------|--------|",
        ]

        for i, pq in enumerate(worst, start=1):
            q_short = pq.question[:60] + ("…" if len(pq.question) > 60 else "")
            lines.append(
                f"| {i} | {q_short} | `{pq.average:.3f}` | `{pq.faithfulness:.3f}` "
                f"| `{pq.answer_relevancy:.3f}` | `{pq.context_precision:.3f}` "
                f"| `{pq.context_recall:.3f}` |"
            )

        lines += [
            "",
            "---",
            "",
            "## Per-Question Scores",
            "",
            "| Question | Faithfulness | Relevancy | Precision | Recall |",
            "|----------|--------------|-----------|-----------|--------|",
        ]

        for pq in results.per_question:
            q_short = pq.question[:70] + ("…" if len(pq.question) > 70 else "")
            lines.append(
                f"| {q_short} | `{pq.faithfulness:.3f}` | `{pq.answer_relevancy:.3f}` "
                f"| `{pq.context_precision:.3f}` | `{pq.context_recall:.3f}` |"
            )

        lines += ["", "---", "", "_Generated by CropFresh RAGAS Evaluation Framework_"]
        return "\n".join(lines) + "\n"

    # ------------------------------------------------------------------
    # Private — JSON metadata
    # ------------------------------------------------------------------

    def _build_meta(self, results: EvalResults) -> EvalReportMeta:
        targets = results.meets_targets()
        return EvalReportMeta(
            generated_at=datetime.now(timezone.utc).isoformat(),
            dataset_path=results.dataset_path,
            num_questions=len(results.per_question),
            faithfulness=results.faithfulness,
            answer_relevancy=results.answer_relevancy,
            context_precision=results.context_precision,
            context_recall=results.context_recall,
            overall_score=results.overall_score,
            all_targets_met=all(targets.values()),
            per_question=[pq.model_dump() for pq in results.per_question],
        )
