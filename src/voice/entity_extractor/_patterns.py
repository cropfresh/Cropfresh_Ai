"""
Quantity, Price, and Location regex patterns for 10 Indian languages.
Also contains UNIT_MAP for normalising raw unit strings to standard codes.
"""

# ---------------------------------------------------------------------------
# Quantity patterns  →  group(1) = value, group(2) = raw unit
# ---------------------------------------------------------------------------
QUANTITY_PATTERNS: dict[str, str] = {
    "hi": r"(\d+(?:\.\d+)?)\s*(किलो|क्विंटल|टन|kg|quintal)",
    "kn": r"(\d+(?:\.\d+)?)\s*(ಕೆಜಿ|ಕ್ವಿಂಟಲ್|kg|quintal)",
    "en": r"(\d+(?:\.\d+)?)\s*(kg|kilos?|quintals?|tons?)",
    "ta": r"(\d+(?:\.\d+)?)\s*(கிலோ|குவிண்டால்|kg)",
    "te": r"(\d+(?:\.\d+)?)\s*(కిలో|క్వింటాల్|kg)",
    "mr": r"(\d+(?:\.\d+)?)\s*(किलो|क्विंटल|kg)",
    "bn": r"(\d+(?:\.\d+)?)\s*(কেজি|কুইন্টাল|kg)",
    "gu": r"(\d+(?:\.\d+)?)\s*(કિલો|ક્વિન્ટલ|kg)",
    "pa": r"(\d+(?:\.\d+)?)\s*(ਕਿਲੋ|ਕੁਇੰਟਲ|kg)",
    "ml": r"(\d+(?:\.\d+)?)\s*(കിലോ|ക്വിന്റൽ|kg)",
}

# ---------------------------------------------------------------------------
# Price patterns  →  group(1) = numeric value
# ---------------------------------------------------------------------------
PRICE_PATTERNS: dict[str, str] = {
    "hi": r"(?:₹|रुपये|रुपया|price|भाव)\s*(\d+(?:\.\d+)?)",
    "kn": r"(?:₹|ರೂ|ಬೆಲೆ|price)\s*(\d+(?:\.\d+)?)",
    "en": r"(?:₹|rs|rupees|price)\s*(\d+(?:\.\d+)?)",
    "ta": r"(?:₹|ரூபாய்|விலை)\s*(\d+(?:\.\d+)?)",
    "te": r"(?:₹|రూపాయలు|ధర)\s*(\d+(?:\.\d+)?)",
    "mr": r"(?:₹|रुपये|भाव)\s*(\d+(?:\.\d+)?)",
    "bn": r"(?:₹|টাকা|দাম)\s*(\d+(?:\.\d+)?)",
    "gu": r"(?:₹|રૂપિયા|ભાવ)\s*(\d+(?:\.\d+)?)",
    "pa": r"(?:₹|ਰੁਪਏ|ਭਾਅ)\s*(\d+(?:\.\d+)?)",
    "ml": r"(?:₹|രൂപ|വില)\s*(\d+(?:\.\d+)?)",
}

# ---------------------------------------------------------------------------
# Location patterns  →  group(1) = location name
# ---------------------------------------------------------------------------
LOCATION_PATTERNS: dict[str, str] = {
    "hi": r"(?:में|से|का|की|के)\s+(\w+)",
    "kn": r"(?:ಯಲ್ಲಿ|ಇಂದ)\s+(\w+)",
    "en": r"(?:in|at|from)\s+(\w+)",
    "ta": r"(?:இல்|ல்|இலிருந்து)\s+(\w+)",
    "te": r"(?:లో|నుండి)\s+(\w+)",
    "mr": r"(?:मध्ये|मधून)\s+(\w+)",
    "bn": r"(?:তে|থেকে)\s+(\w+)",
    "gu": r"(?:માં|થી)\s+(\w+)",
    "pa": r"(?:ਵਿੱਚ|ਤੋਂ)\s+(\w+)",
    "ml": r"(?:ൽ|നിന്ന്)\s+(\w+)",
}

